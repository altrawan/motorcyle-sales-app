    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        if (tabel == false) {
            Messages.setErrorMessage("Pilih data dari tabel terlebih dahulu..!");
        } else {
            int row = tbl.getSelectedRow();
            FrmMasterPelanggan m = new FrmMasterPelanggan();
            m.aksi = "UPDATE";
            m.setTitle("Edit Data");
            m.kode = tbl.getValueAt(row, 0).toString();
            m.nik = tbl.getValueAt(row, 1).toString();
            m.nama = tbl.getValueAt(row, 2).toString();
            m.jk = tbl.getValueAt(row, 3).toString();
            m.alamat = tbl.getValueAt(row, 4).toString();
            m.kk = tbl.getValueAt(row, 5).toString();
            m.telp = tbl.getValueAt(row, 6).toString();
            m.ket = tbl.getValueAt(row, 7).toString();;
            m.status = tbl.getValueAt(row, 8).toString();
            m.show();
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        if (tabel == false) {
            Messages.setErrorMessage("Pilih data dari tabel terlebih dahulu..!");
        } else {
            int row = tbl.getSelectedRow();
            if (Messages.setConfirmMessage("Apakah Anda yakin akan menghapus data dengan nama " + tbl.getValueAt(row, 2) + " ?")) {
                try {
                    Pelanggan k = new Pelanggan();
                    k.setKode(tbl.getValueAt(row, 0).toString());
                    if (status.equals("AKTIF")) {
                        Messages.setErrorMessage("Data tersebut masih AKTIF\nMohon Non-Aktifkan data jika ingin menghapusnya");
                    } else {
                        i.delete(k.getKode());
                        Messages.setInfomationMessage("Data telah berhasil dihapus");
                    }
                    initPagination();
                } catch (SQLException ex) {
                    Messages.setErrorMessage("Ada kesalahan dalam SQL statement..!");
                }
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

