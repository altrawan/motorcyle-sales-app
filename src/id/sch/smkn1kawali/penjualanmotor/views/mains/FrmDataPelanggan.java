    private void initPagination() {
        try {
            totalData = i.count();
            jumlahBarisPerhalaman = Integer.valueOf(jComboBox1.getSelectedItem().toString());
            Double totalHalamanD = Math.ceil(totalData.doubleValue()/jumlahBarisPerhalaman.doubleValue());
            totalHalaman = totalHalamanD.intValue();
            if(nomorHalaman.equals(1)) {
                btnAwal.setEnabled(false);
                btnMundur.setEnabled(false);
            } else {
                btnAwal.setEnabled(true);
                btnMundur.setEnabled(true);
            }

            if(nomorHalaman.equals(totalHalaman)) {
                btnAkhir.setEnabled(false);
                btnMaju.setEnabled(false);
            } else {
                btnAkhir.setEnabled(true);
                btnMaju.setEnabled(true);
            }
            
            if(nomorHalaman>totalHalaman) {
                nomorHalaman = 1;
            }
            // TAMPIL DATA
            TablePelanggan t = new TablePelanggan(i.findAll(nomorHalaman, jumlahBarisPerhalaman));
            tbl.setModel(t);
            tbl.getColumnModel().getColumn(9).setCellRenderer(new DateRenderer());
            lblInfo.setText("Halaman " + nomorHalaman + " dari " + totalHalaman + " Total Data : " + totalData);
        } catch (SQLException ex) {
            ex.toString();
        }
    }
    
