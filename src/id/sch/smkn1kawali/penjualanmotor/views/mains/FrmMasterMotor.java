    private void buatnomor() {
        try {
            Motor m = new Motor();
            i.createId(m);
            tKode.setText(m.getKode());
        } catch (SQLException ex) {
            Messages.setErrorMessage("Ada kesalahan dalam SQL statement..!");
        }
    }
    
    private void bersih() {
        buatnomor();
        cbJenis.setSelectedIndex(0);
        cbTipe.setSelectedIndex(0);
        cbWarna.setSelectedIndex(0);
        tHarga.setText("");
        tGambar.setText("");
        lGambar.setIcon(null);
        tStok.setText("");
        buttonGroup2.clearSelection();
        tKet.setText("");
    }
    
    private void setEditOff() {
        cbJenis.setEnabled(false);
        cbTipe.setEnabled(false);
        cbWarna.setEnabled(false);
        tHarga.setEnabled(false);
        rbAktif.setEnabled(false);
        rbTidakAktif.setEnabled(false);
        tKet.setEnabled(false);
    }
    
    private void edit() {
        tKode.setText(kode);
        cbJenis.setSelectedItem(jenis);
        cbTipe.setSelectedItem(tipe);
        cbWarna.setSelectedItem(warna);
        tHarga.setText(harga.toString());
        tGambar.setText(gambar);
        lGambar.setIcon(resizeImage("src/id/sch/smkn1kawali/penjualanmotor/resources/" + gambar));
        tStok.setText(stok.toString());
        if (status.equals("AKTIF")) {
            rbAktif.setSelected(true);
        } else {
            rbTidakAktif.setSelected(true);
        }
        tKet.setText(ket);
    }
    
    private void validasi() {
        switch (aksi) {
            case "INSERT":
                bersih();
                jLabel1.setText("Tambah Data");
                break;
            case "UPDATE":
                edit();
                jLabel1.setText("Edit Data");
                break;
            default:
                edit();
                setEditOff();
                jLabel1.setText("View Data");
                btnSimpan.setVisible(false);
                break;
        }
    }
    
    private ImageIcon resizeImage(String ImagePath) {
        ImageIcon icon = new ImageIcon(ImagePath);
        Image image = icon.getImage();
        Image img = image.getScaledInstance(lGambar.getWidth(), lGambar.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon imageIcon = new ImageIcon(img);
        return imageIcon;
    }
    

