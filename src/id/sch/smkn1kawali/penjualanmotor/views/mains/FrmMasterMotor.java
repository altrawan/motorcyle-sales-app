    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if (cbJenis.getSelectedIndex() == 0 || cbTipe.getSelectedIndex() == 0
                || cbWarna.getSelectedIndex() == 0 || tHarga.getText().isEmpty()
                || tGambar.getText().isEmpty() || lGambar.getIcon() == null
                || tStok.getText().isEmpty() || rbAktif.isSelected() == false
                && rbTidakAktif.isSelected() == false || tKet.getText().isEmpty()) {
            Messages.setErrorMessage("Lengkapi data terlebih dahulu..!");
        } else if (tHarga.getText().length() < 7 || tHarga.getText().length() > 8) {
            Messages.setErrorMessage("Harga berisi 7-8 digit angka");
        } else {
            String status;
            if (rbAktif.isSelected()) {
                status = "AKTIF";
            } else {
                status = "TIDAK AKTIF";
            }
            try {
                int hrg = Integer.parseInt(tHarga.getText());
                int stok = Integer.parseInt(tStok.getText());
                Motor m = new Motor();
                m.setKode(tKode.getText());
                m.setJenis(cbJenis.getSelectedItem().toString());
                m.setType(cbTipe.getSelectedItem().toString());
                m.setWarna(cbWarna.getSelectedItem().toString());
                m.setHarga(hrg);
                m.setStok(stok);
                m.setStatus(status);
                m.setKeterangan(tKet.getText());
                if ("INSERT".equals(aksi)) {
                    //Menambah record baru ke database
                    m.setGambar(name + ".png");
                    ImageIO.write(image, "png", new File("src/id/sch/smkn1kawali/penjualanmotor/resources/" + name + ".png"));
                    i.insert(m);
                    Messages.setInfomationMessage("Data telah berhasil disimpan");
                    bersih();
                } else {
                    //Mengubah record yang sudah ada di database
                    if (tGambar.getText().equals(gambar)) {
                        m.setGambar(tGambar.getText());
                        i.update(m);
                        dispose();
                    } else {
                        File file = new File("src/id/sch/smkn1kawali/penjualanmotor/resources/" + gambar);
                        file.delete();
                        m.setGambar(name + ".png");
                        ImageIO.write(image, "png", new File("src/id/sch/smkn1kawali/penjualanmotor/resources/" + name + ".png")); 
                        i.update(m);
                        dispose();
                    }
                    Messages.setInfomationMessage("Data telah berhasil diperbaharui");
                }
            } catch (SQLException ex) {
                Messages.setErrorMessage("Ada kesalahan dalam SQL statement..!" + ex.toString());
            } catch (IOException ex) {
                Logger.getLogger(ControllerMotor.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

