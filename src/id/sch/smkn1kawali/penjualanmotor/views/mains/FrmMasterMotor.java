        FileNameExtensionFilter filter = new FileNameExtensionFilter("Image Files(*.jpg; *.jpeg; *.png; *.gif; *.bmp)", "jpg", "jpeg", "png", "gif", "bmp");
        chooser.setFileFilter(filter);
        int result = chooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            File file = chooser.getSelectedFile();
            Date tgl = new Date();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyyMMddHHmmss");
            name = sdf.format(tgl);
            tGambar.setText(file.getName());
            try {
                image = ImageIO.read(file);
                
            } catch (IOException ex) {
                Logger.getLogger(FrmMasterMotor.class.getName()).log(Level.SEVERE, null, ex);
            }
            String path = file.getAbsolutePath();
            lGambar.setIcon(resizeImage(path));
        }
