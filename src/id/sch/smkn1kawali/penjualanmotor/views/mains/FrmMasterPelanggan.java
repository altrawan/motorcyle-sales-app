    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if (tKode.getText().isEmpty() || tNik.getText().isEmpty() || tNama.getText().isEmpty() 
                || tAlamat.getText().isEmpty() ||  rbLaki.isSelected() == false
                &&  rbPerempuan.isSelected() == false ||  tKK.getText().isEmpty()
                ||  tTelepon.getText().isEmpty() ||  rbAktif.isSelected() == false 
                && rbTidakAktif.isSelected() == false || tKet.getText().isEmpty()) {
            Messages.setErrorMessage("Lengkapi data terlebih dahulu..!");
        } else if (tNik.getText().length() != 16) {
            Messages.setErrorMessage("Nik wajib berisi 16 digit..!");
        } else if (tNama.getText().length() < 3 || tNama.getText().length() > 50) {
            Messages.setErrorMessage("Nama berisi 3-50 digit..!");
        } else if (tKK.getText().length() != 10) {
            Messages.setErrorMessage("No.KK wajib berisi 10 digit..!");
        } else if (tTelepon.getText().length() < 11 || tTelepon.getText().length() > 12) {
            Messages.setErrorMessage("Nomor Telepon berisi 11-12 digit..!");
        } else {
            String jenis, status;
            if (rbLaki.isSelected()) {
                jenis = "Laki-Laki";
            } else {
                jenis = "Perempuan";
            }
            if (rbAktif.isSelected()) {
                status = "AKTIF";
            } else {
                status = "TIDAK AKTIF";
            }
            try {
                Pelanggan k = new Pelanggan();
                k.setKode(tKode.getText());
                k.setNik(tNik.getText());
                k.setNama(tNama.getText());
                k.setJk(jenis);
                k.setAlamat(tAlamat.getText());
                k.setKk(tKK.getText());
                k.setTelepon(tTelepon.getText());
                k.setKeterangan(tKet.getText());
                k.setStatus(status);
                if ("INSERT".equals(aksi)) {
                    //Menambah record baru ke database
                    i.insert(k);
                    Messages.setInfomationMessage("Data telah berhasil disimpan");
                    bersih();
                } else {
                    //Mengubah record yang sudah ada di database
                    i.update(k);
                    Messages.setInfomationMessage("Data telah berhasil diperbaharui");
                    dispose();
                }
            } catch (SQLException ex) {
                Messages.setErrorMessage("Ada kesalahan dalam SQL statement..!" + ex.toString());
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

