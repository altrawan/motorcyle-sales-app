    private void buatnomor() {
        try {
            Pelanggan k = new Pelanggan();
            i.createId(k);
            tKode.setText(k.getKode());
        } catch (SQLException ex) {
            Messages.setErrorMessage("Ada kesalahan dalam SQL statement..!");
        }
    }
    
    private void bersih() {
        buatnomor();
        tNik.setText("");
        tNama.setText("");
        buttonGroup1.clearSelection();
        tAlamat.setText("");
        tKK.setText("");
        tTelepon.setText("");
        buttonGroup2.clearSelection();
        tKet.setText("");
    }
    
    private void setEditOff() {
        tNik.setEnabled(false);
        tNama.setEnabled(false);
        rbLaki.setEnabled(false);
        rbPerempuan.setEnabled(false);
        tAlamat.setEnabled(false);
        tKK.setEnabled(false);
        tTelepon.setEnabled(false);
        rbAktif.setEnabled(false);
        rbTidakAktif.setEnabled(false);
        tKet.setEnabled(false);
    }
    
    private void edit() {
        tKode.setText(kode);
        tNik.setText(nik);
        tNama.setText(nama);
        if (jk.equals("Laki-Laki")) {
            rbLaki.setSelected(true);
        } else {
            rbPerempuan.setSelected(true);
        }
        tAlamat.setText(alamat);
        tKK.setText(kk);
        tTelepon.setText(telp);
        if (status.equals("AKTIF")) {
            rbAktif.setSelected(true);
        } else {
            rbTidakAktif.setSelected(true);
        }
        tKet.setText(ket);
    }
    
    private void validasi() {
        switch (aksi) {
            case "INSERT":
                bersih();
                jLabel1.setText("Tambah Data");
                break;
            case "UPDATE":
                edit();
                jLabel1.setText("Edit Data");
                break;
            default:
                edit();
                setEditOff();
                jLabel1.setText("View Data");
                btnSimpan.setVisible(false);
                break;
        }
    }

